<?php

namespace Nether\Atlantis\Media;

use Nether\Common;

use JsonSerializable;

class ExtraFile
extends Common\Prototype
implements JsonSerializable {

	const
	TypeImgSet = 'imgset';

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	public string
	$Type;

	public int
	$Size;

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	protected function
	OnReady(Common\Prototype\ConstructArgs $Argv):
	void {

		return;
	}

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	public function
	JsonSerialize():
	mixed {

		return [ 'Type'=> $this->Type, 'Size'=> $this->Size ];
	}

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	static public function
	FromJSON(string $Input):
	static {

		$Data = json_decode($Input);

		return new static($Data);
	}

}